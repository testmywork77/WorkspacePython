# List Example 
fruits = ["apple", "banana", "cherry"] 
print("List Example:", fruits)
print("List Data Type:", type(fruits))
 
# Tuple Example
coordinates = (3, 7)
print("Tuple Example:", coordinates)
print("Tuple Data Type:", type(coordinates))
 
 # Dictionary Example 
person = {"name": "Alice", "age": 25, "is_student": True}
print("Dictionary Example:", person)
print("Dictionary Data Type:", type(person))