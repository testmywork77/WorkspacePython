# Create a Class
class Student:  
    sno=1
    sname="Abhinav"        
    age=13
    loc="Aylesbury"
    subjects=["Computers", "Maths", "Physics", "Chemistry"]

# Create Object
student = Student()

print(f"No: {student.sno}")
print(f"Name: {student.sname}")
print(f"Age: {student.age}")
print(f"Location: {student.loc}")
print(f"Subjects: {student.subjects}")